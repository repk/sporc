.section .text, "ax", @progbits

tmain:
	/* Enable trap */
	rd %psr, %g1
	wr %g1, 0x20, %psr
	nop; nop; nop

	sethi %hi(traptbl), %g1
	wr %g1, %lo(traptbl), %tbr
	or %g0, %g0, %g1
	ta 4
	/* Hopefully this is a deadspot */
	sethi %hi(0xb16b00b5), %g1
	or %g1, %lo(0xb16b00b5), %g1

